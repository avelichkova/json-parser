#pragma once

#include "Commands.h"
#include <sstream>

/**
 * @brief Class representing a Runner for executing commands.
 * 
 * The Runner class provides a static method 'run' to execute commands.
 */
class Runner
{
public:
    /**
     * @brief Executes the predefined commands.
     * 
     * This static method is responsible for executing a sequence of predefined commands.
     * It serves as the entry point for running the application.
     */
    static void run();
};


